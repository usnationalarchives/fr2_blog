require_once(dirname(__FILE__).'/../../vendor/php/yaml/lib/sfYamlParser.php');
$yaml = new sfYamlParser();
$config = $yaml->parse(file_get_contents(dirname(__FILE__).'/../../config/wp-config.yml'));

foreach($config['database'] as $db_variable => $value) {
    define(('DB_' . strtoupper($db_variable)), $value);
}

# must have this for WP behind a proxy to realize it's on ssl.
# the front end proxy should set X-Forwarded-Proto to https when using SSL 
if ($_SERVER['HTTP_X_FORWARDED_PROTO'] == 'https') {
    $_SERVER['HTTPS'] = 'on';
}


/**#@+
 * Authentication Unique Keys and Salts.
 *
 * Change these to different unique phrases!
 * You can generate these using the {@link https://api.wordpress.org/secret-key/1.1/salt/ WordPress.org secret-key service}
 * You can change these at any point in time to invalidate all existing cookies. This will force all users to have to log in again.
 *
 * @since 2.6.0
 */
foreach($config['wordpress'] as $wp_variable => $value) {
    define(strtoupper($wp_variable), $value);
}

/**#@-*/

/**
 * WordPress Database Table prefix.
 *
 * You can have multiple installations in one database if you give each a unique
 * prefix. Only numbers, letters, and underscores please!
 */
